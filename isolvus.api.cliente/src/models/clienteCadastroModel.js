export async function setAlterarGeolocalizacao(jsonReq) {

  
  const ssqlAlterar = `
    UPDATE PCCLIENT CL SET CL.LATITUDE = :latitude, CL.LONGITUDE = :longitude WHERE CL.CODCLI = :codcli
  `;

  const param = {
    latitude: jsonReq.latitude,
    longitude: jsonReq.longitude,
    codcli: jsonReq.id_cliente
  }

  try {

    await executeQuery(ssqlAlterar, param, true);
    return {mensagem: "Geolocalização alterada com sucesso !"}

  } catch (error) {
    console.log(error);
    throw error;
  }

}

export async function buscarClientePorCGC(cgcEnt) {
  const connection = await getConnection();

  const cgcEntA = (cgcEnt || '').replace(/\D/g, '');

  try {
    const query = `
      SELECT CODCLI,
       CGCENT,
       CLIENTE,
       TELENT,
       CODPLPAG,
       CODPRACA,
       CODUSUR1,
       ENDERENT,
       BAIRROENT,
       MUNICENT,
       ESTENT,
       CEPENT
      FROM PCCLIENT
      WHERE REGEXP_REPLACE(CGCENT, '[^0-9]', '') = REGEXP_REPLACE(:cgcEnt, '[^0-9]', '')
      ORDER BY CODCLI DESC
    `;

    const result = await connection.execute(query, { cgcEnt: cgcEntA }, {
      outFormat: oracledb.OUT_FORMAT_OBJECT
    });

    return result.rows[0] || null;

  } catch (error) {
    console.error("Erro ao buscar cliente:", error);
    throw error;
  } finally {
    await connection.close();
  }
}

